# doctor  —  Pre-flight check
# ═══════════════════════════════════════════════════════════════════════════════


@app.command()
def doctor() -> None:
    """Run a comprehensive pre-flight check and report system health."""
    console.print(Panel.fit("[bold blue]\U0001fa7a  GetAJob Doctor - Pre-Flight Check[/]", border_style="blue"))

    async def _run_doctor() -> None:
        checks: list[tuple[str, str, str]] = []
        critical_fail = False
        settings_obj = None

        v = sys.version_info
        if v >= (3, 12):
            checks.append(("Python Version", "\u2705", f"Python {sys.version.split()[0]}"))
        else:
            checks.append(("Python Version", "\u274c", f"Python {sys.version.split()[0]} (< 3.12)"))
            critical_fail = True

        try:
            settings_obj = get_settings()
            api_key = settings_obj.llm.api_key or os.environ.get("ANTHROPIC_API_KEY", "")
            if api_key:
                checks.append(("LLM API Key", "\u2705", f"Provider: {settings_obj.llm.provider}"))
            else:
                checks.append(("LLM API Key", "\u274c", "No API key found"))
                critical_fail = True
        except Exception as exc:
            checks.append(("LLM API Key", "\u274c", f"Config error: {exc}"))
            critical_fail = True

        if settings_obj is not None:
            try:
                from sqlalchemy import text
                async def _probe_db() -> None:
                    engine = create_engine()
                    try:
                        async with engine.connect() as conn:
                            await conn.execute(text("SELECT 1"))
                    finally:
                        await engine.dispose()
                try:
                    await asyncio.wait_for(_probe_db(), timeout=5.0)
                    db = settings_obj.database
                    checks.append(("Database", "\u2705", f"Connected: {db.host}:{db.port}"))
                except TimeoutError:
                    checks.append(("Database", "\u274c", "Timed out after 5s"))
                    critical_fail = True
                except Exception as exc:
                    checks.append(("Database", "\u274c", f"Failed: {exc}"))
                    critical_fail = True
            except Exception as exc:
                checks.append(("Database", "\u274c", f"Config error: {exc}"))
                critical_fail = True

        project_root = Path(__file__).resolve().parent.parent.parent
        env_ok = (project_root / ".env").exists()
        yaml_ok = (project_root / "config" / "settings.yaml").exists()
        checks.append((
            "Config Files",
            "\u2705" if env_ok else "\u26a0",
            f"{'E' if env_ok else 'M'} .env  {'Y' if yaml_ok else 'M'} settings.yaml",
        ))

        import importlib.util
        checks.append(("Browser", "\u2705" if importlib.util.find_spec("playwright") else "\u274c",
                       "Playwright installed" if importlib.util.find_spec("playwright") else "Not installed"))

        data_dir = settings_obj.data_dir if settings_obj else Path.cwd() / "data"
        missing_dirs = [d for d in ("data", "data/profiles", "data/resumes", "data/cover_letters")
                        if not (data_dir / d if d != "data" else data_dir).exists()
                       ] if settings_obj else []
        checks.append(("Data Dirs", "\u2705" if not missing_dirs else "\u26a0",
                       "All present" if not missing_dirs else f"Missing: {', '.join(missing_dirs)}"))

        import shutil
        checks.append(("Docker", "\u2705" if shutil.which("docker") else "-",
                       "Found" if shutil.which("docker") else "Not found (optional)"))

        table = Table(title="System Health", border_style="blue")
        table.add_column("Check", style="cyan", no_wrap=True)
        table.add_column("Status", justify="center")
        table.add_column("Detail")

        status_colors = {"\u2705": "[green]\u2705[/]", "\u274c": "[red]\u274c[/]",
                         "\u26a0": "[yellow]\u26a0[/]", "-": "[dim]-[/]"}
        for name, status, detail in checks:
            table.add_row(name, status_colors.get(status, status), detail)
        console.print()
        console.print(table)
        console.print()

        if critical_fail:
            console.print(Panel("[bold red]\u2717  Some critical checks failed.[/]\n\n"
                                "  Run [cyan]getajob init[/] to set up your configuration.",
                                border_style="red", title="Doctor Summary"))
        elif any(s in ("\u26a0", "\u274c") for _, s, _ in checks if s != "-"):
            console.print(Panel("[bold yellow]\u26a0  All critical checks passed, but some items need attention.[/]",
                                border_style="yellow", title="Doctor Summary"))
        else:
            console.print(Panel("[bold green]\u2713  All checks passed![/]  Your system is ready.",
                                border_style="green", title="Doctor Summary"))

    _run_async(_run_doctor())


