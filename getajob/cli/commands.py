# init  —  First-run wizard
# ═══════════════════════════════════════════════════════════════════════════════


@app.command()
def init(
    force: bool = typer.Option(
        False,
        "--force",
        "-f",
        help="Overwrite existing .env without prompting.",
    ),
) -> None:
    """Interactive first-run wizard for non-technical users.

    Walks through the initial configuration: LLM API key, profile creation,
    default search vectors, and database setup -- all in one step.
    """
    console.print(
        Panel.fit(
            "[bold green]\U0001f680  GetAJob -- First-Run Init[/]",
            border_style="green",
        )
    )

    project_root = Path(__file__).resolve().parent.parent.parent
    env_path = project_root / ".env"

    # -- Step 1: Check for existing .env -----------------------------------
    if env_path.exists() and not force:
        overwrite = typer.confirm(
            f"  {env_path} already exists.\n  Overwrite existing configuration?",
            default=False,
        )
        if not overwrite:
            console.print("[yellow]  Init cancelled -- no changes made.[/]")
            raise typer.Exit()

    # -- Step 2: LLM provider & API key ------------------------------------
    console.print("\n[bold]\U0001f916  LLM Configuration[/]")

    existing_key = os.environ.get("GETAJOB_LLM__API_KEY")
    if existing_key:
        api_key = existing_key
        provider = os.environ.get("GETAJOB_LLM__PROVIDER", "anthropic")
        console.print("  [dim]Using GETAJOB_LLM__API_KEY from environment[/]")
    else:
        provider = typer.prompt("  LLM provider", default="anthropic")
        api_key = typer.prompt("  API key", hide_input=True)

    # -- Step 3: Write .env file -------------------------------------------
    model = "claude-sonnet-4-6" if provider.lower() in ("anthropic", "claude") else "gpt-4o"
    encryption_key = os.urandom(32).hex()
    encryption_salt = os.urandom(16).hex()
    tokenizer_salt = os.urandom(16).hex()

    env_content = f"""# GetAJob -- Environment Configuration
# Auto-generated by `getajob init`

GETAJOB_ENVIRONMENT=development
GETAJOB_DEBUG=true
GETAJOB_LOG_FORMAT=console

# -- PostgreSQL ----------------------------------------------------------------
GETAJOB_DATABASE__HOST=localhost
GETAJOB_DATABASE__PORT=5432
GETAJOB_DATABASE__DATABASE=getajob
GETAJOB_DATABASE__USER=getajob
GETAJOB_DATABASE__PASSWORD=change_me_in_production
GETAJOB_DATABASE__MIN_CONNECTIONS=2
GETAJOB_DATABASE__MAX_CONNECTIONS=10

# -- LLM -----------------------------------------------------------------------
GETAJOB_LLM__PROVIDER={provider}
GETAJOB_LLM__API_KEY={api_key if not existing_key else ""}
GETAJOB_LLM__MODEL={model}
GETAJOB_LLM__MAX_TOKENS=4096
GETAJOB_LLM__TEMPERATURE=0.7
GETAJOB_LLM__TIMEOUT_SECONDS=120

# -- Redis ---------------------------------------------------------------------
GETAJOB_REDIS__HOST=localhost
GETAJOB_REDIS__PORT=6379
GETAJOB_REDIS__DB=0

# -- Security / Encryption (auto-generated) ------------------------------------
GETAJOB_SECURITY__ENCRYPTION_KEY={encryption_key}
GETAJOB_SECURITY__ENCRYPTION_SALT={encryption_salt}
GETAJOB_SECURITY__TOKENIZER_SALT={tokenizer_salt}
# GETAJOB_SECURITY__APPROVAL_PASSWORD=

# -- Browser Automation --------------------------------------------------------
GETAJOB_BROWSER__HEADLESS=true
GETAJOB_BROWSER__VIEWPORT_WIDTH=1920
GETAJOB_BROWSER__VIEWPORT_HEIGHT=1080
GETAJOB_BROWSER__LOCALE=en-US
GETAJOB_BROWSER__SLOW_MO_MS=50
GETAJOB_BROWSER__NAVIGATION_TIMEOUT_SECONDS=30

# -- Job Discovery -------------------------------------------------------------
GETAJOB_JOB_DISCOVERY__MAX_APPLICATIONS_PER_DAY=50
"""
    env_path.write_text(env_content)
    env_path.chmod(0o600)
    console.print(f"  [green]\u2713[/] Configuration written: {env_path}")

    # -- Step 4: User profile (optional) -----------------------------------
    console.print("\n[bold]\U0001f464  User Profile (optional)[/]")
    console.print("  Provide your name and email to create a profile.")
    name = typer.prompt("  Name", default="")
    email = typer.prompt("  Email", default="")
    has_profile = bool(name and email)

    # -- Steps 5-7: Async operations ---------------------------------------
    async def _run_init() -> None:
        import core.config as core_config

        core_config._settings = None

        if has_profile:
            try:
                from core.schemas import ProfileCreate
                from profile_engine.profile_store import ProfileStore

                engine = create_engine()
                store = ProfileStore(engine)
                profile = await store.create_profile(
                    ProfileCreate(
                        name=name,
                        email=email,
                        phone="",
                    )
                )
                console.print(
                    f"  [green]\u2713[/] Profile created: [bold]{profile.name}[/] (id: {profile.id})"
                )
                await engine.dispose()
            except Exception as exc:
                console.print(f"  [yellow]\u26a0[/] Could not create profile: {exc}")
                console.print("    You can create one later by creating a profile directly.")

        try:
            _write_default_search_vectors(project_root)
            console.print("  [green]\u2713[/] Default search vectors written to settings.yaml")
        except Exception as exc:
            console.print(f"  [yellow]\u26a0[/] Could not write search vectors: {exc}")

        try:
            s = get_settings()
            engine = create_engine()
            await run_migrations(engine)

            data_dir = s.data_dir
            data_dir.mkdir(parents=True, exist_ok=True)
            for sub in ("profiles", "resumes", "cover_letters", "screenshots"):
                (data_dir / sub).mkdir(parents=True, exist_ok=True)

            console.print("  [green]\u2713[/] Database schema ready.")
            await engine.dispose()
        except Exception as exc:
            console.print(f"  [yellow]\u26a0[/] Could not initialise database: {exc}")
            console.print(
                "    Ensure PostgreSQL is running and .env is configured correctly."
            )

    _run_async(_run_init())

    # -- Step 8: Success summary -------------------------------------------
    console.print()
    console.print(
        Panel(
            "[bold green]\u2713[/]  GetAJob initialised successfully!\n\n"
            "  [bold]Next steps:[/]\n"
            "  \u2022 Run [cyan]getajob run[/]        "
            "-- Start discovering jobs\n"
            "  \u2022 Run [cyan]getajob serve[/]      "
            "-- Open the approval queue\n"
            "  \u2022 Edit [cyan].env[/]              "
            "-- Advanced configuration\n"
            "  \u2022 See [cyan]env.template[/]       "
            "-- All available settings\n",
            border_style="green",
            title="Init Complete",
        )
    )


# ═══════════════════════════════════════════════════════════════════════════════
# discover  —  Job discovery only
# ═══════════════════════════════════════════════════════════════════════════════


@app.command()
def discover(
    _ctx: typer.Context,
    continuous: bool = typer.Option(
        False,
        "--continuous",
        "-c",
        help="Run discovery continuously on an interval.",
    ),
    interval: float = typer.Option(
        15.0,
        "--interval",
        "-i",
        help="Minutes between discovery passes in continuous mode.",
        min=1.0,
        max=120.0,
    ),
) -> None:
    """Discover job listings from all configured search vectors.

    Executes the orchestrator's discovery phase: loads search vectors from
    config, queries each job source for new listings, runs context analysis
    against the active profile, and creates Application records in DISCOVERED
    state.
    """
    console.print(Panel.fit("[bold blue]\U0001f50d  Job Discovery[/]", border_style="blue"))
    _check_settings()

    async def _run() -> None:
        engine = create_engine()
        event_bus = InMemoryEventBus()
        llm_client = get_llm_client()

        orchestrator = OrchestratorAgent(
            engine=engine,
            llm_client=llm_client,
            event_bus=event_bus,
        )

        await orchestrator.start()
        try:
            if continuous:
                console.print(
                    "[blue]\u27f3[/] Continuous discovery mode -- press Ctrl+C to stop gracefully.\n"
                )
                interval_seconds = interval * 60.0
                while True:
                    result = await orchestrator.run_once()
                    _print_result_table(result, title="Discovery Pass Complete")
                    console.print(f"[dim]Sleeping {interval:.0f} min until next pass...[/]\n")
                    await asyncio.sleep(interval_seconds)
            else:
                with Progress(
                    SpinnerColumn(),
                    TextColumn("[progress.description]{task.description}"),
                    console=console,
                    transient=True,
                ) as progress:
                    progress.add_task(description="Discovering jobs...", total=None)
                    result = await orchestrator.run_once()

                _print_result_table(result, title="Discovery Results")
        finally:
            await orchestrator.stop()

    _run_async(_run())


# ═══════════════════════════════════════════════════════════════════════════════
# tailor  —  Tailor materials for a specific job
# ═══════════════════════════════════════════════════════════════════════════════


@app.command()
def tailor(
    _ctx: typer.Context,
    job_id: str = typer.Argument(
        ...,
        help="UUID of the job listing to tailor.",
    ),
) -> None:
    """Generate tailored resume and cover letter for a specific job.

    Runs the full analysis + tailoring pipeline for a single job listing
    that has already been discovered.
    """
    console.print(Panel.fit("[bold green]\U0001f4dd  Job Tailoring[/]", border_style="green"))
    _check_settings()

    async def _run() -> None:
        from sqlalchemy import select

        from core.database import get_session
        from core.models import JobListing

        engine = create_engine()
        event_bus = InMemoryEventBus()
        llm_client = get_llm_client()

        from agents.context_agent import ContextAgent
        from agents.tailoring_agent import TailoringAgent

        async with get_session(engine) as session:
            stmt = select(JobListing).where(JobListing.id == job_id)
            listing = (await session.execute(stmt)).scalar_one_or_none()

        if listing is None:
            err_console.print(f"[red]\u2717[/] Job listing '{job_id}' not found.")
            raise typer.Exit(code=1)

        rich_format = Panel(
            f"[bold]{listing.title}[/]\n"
            f"  Company: {listing.company}\n"
            f"  Source:  {listing.source}\n"
            f"  URL:     {listing.url or 'N/A'}",
            border_style="green",
            title="Job Details",
        )
        console.print(rich_format)

        with Progress(
            SpinnerColumn(),
            TextColumn("[progress.description]{task.description}"),
            console=console,
            transient=True,
        ) as progress:
            progress.add_task(description="Analysing job requirements...", total=None)
            context_agent = ContextAgent(
                engine=engine,
                llm_client=llm_client,
                event_bus=event_bus,
            )
            await context_agent.start()
            desc_text = listing.description_json.get("raw", "") if listing.description_json else ""
            analysis = await context_agent.analyze(
                job_id=str(listing.id),
                job_description=desc_text,
            )
            await context_agent.stop()

            console.print(f"\n  Match score: [bold]{analysis.match_score:.1%}[/]")
            if analysis.matching_skills:
                console.print(f"  Matching skills: {', '.join(analysis.matching_skills[:10])}")
            if analysis.missing_skills:
                console.print(
                    f"  [yellow]Missing skills: {', '.join(analysis.missing_skills[:5])}[/]"
                )
            if analysis.warnings:
                for w in analysis.warnings:
                    console.print(f"  [yellow]\u26a0 {w}[/]")
            console.print()

            progress.add_task(description="Generating resume & cover letter...", total=None)
            tailoring_agent = TailoringAgent(
                engine=engine,
                llm_client=llm_client,
                event_bus=event_bus,
            )
            await tailoring_agent.start()
            result = await tailoring_agent.tailor(
                job_listing_id=str(listing.id),
                profile_id=analysis.profile_id,
                job_title=listing.title,
                company=listing.company,
                job_description=desc_text,
                generate_cover_letter=True,
            )
            await tailoring_agent.stop()

        cover_letter_status = "\u2713 Generated" if result.cover_letter else "\u2717 Skipped"
        warnings_text = "; ".join(result.warnings) if result.warnings else "None"
        response_panel = Panel(
            f"[bold]Resume:[/] {len(result.resume_text)} chars\n"
            f"[bold]Cover Letter:[/] {cover_letter_status}\n"
            f"[bold]Matched Skills:[/] {', '.join(result.matched_skills[:10]) or 'None'}\n"
            f"[bold]Warnings:[/] {warnings_text}\n",
            border_style="green",
            title="Tailoring Complete",
        )
        console.print(response_panel)

    _run_async(_run())


